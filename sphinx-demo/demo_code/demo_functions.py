def add(a: int, b: int) -> int:
    """
    Add two integers.

    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b


def divide(a: float, b: float) -> float:
    """
    Divide two numbers.

    :raises ZeroDivisionError: If b is zero
    """
    return a / b